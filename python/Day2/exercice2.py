number1 = input("Entrez un premier nombre :") 
number2 = input("Entrez un deuxieme nombre :")

somme = int(number1) + int(number2)

difference = int(number1) - int(number2)

produit = int(number1) * int(number2)

quotient = int(number1) / int(number2)


